        <h1 class="h5 mb-0 text-gray-800">Sobre nosotros</h1>
    </div>

    <p>
        OER Variedades del español / Varieties of Spanish es un sitio web que contiene recursos educativos de acceso abierto (OER, por sus siglas en inglés para Open Educational Resources) dirigido tanto a docentes como a estudiantes de español (como lengua materna o extranjera).
        Este proyecto pertenece contribuir a la descolonización del currículum mediante la introducción de materiales didácticos basados en material audiovisual de todo el mundo hispanohablante:
        <ul>
            <li>Español de España (castellano, andaluz y canario)</li>
            <li>Español de América (caribeño, mexicano-centroamericano, andino, austral o rioplatense y chileno) y de Estados Unidos</li>
            <li>Español de Guinea Ecuatorial</li>
            <li>Judeoespañol</li>
        </ul>
    </p>

    <h5> Materiales didácticos </h5>
    <ul>
        <li>Los materiales didácticos han sido elaborados de manera colaborativa entre estudiantes y docentes gracias a la metodología de la clase invertida o flipped classroom, que concede un protagonismo especial al estudiantado.</li>
        <li>La mayoría de los materiales han sido desarrollados por alumnado de lengua española de la Universidad de Granada y posteriormente han sido revisados, seleccionados y mejorados por el profesorado que forma parte de este proyecto.</li>
        <li>Todos los materiales cuentan con una licencia Creative Commons BY-NC-SA (atribución, no comercial, compartir igual).</li>
        <li>Es posible filtrar los materiales por diferentes criterios de búsqueda (pronunciación, gramática, léxico, variedad del español, nivel de español).</li>
    </ul>
    
    <h5>¿Quiénes somos? </h5>
    <p>
    Contamos con un equipo de trabajo multidisciplinar (formado por dos coordinadoras –Rocío Díaz-Bravo y Silvia Acid Carrillo– y veintidós docentes de 8 universidades nacionales e internacionales) con amplia experiencia en la temática de la innovación didáctica propuesta: especialistas en variedades del español, en enseñanza de lenguas (español como lengua materna y ELE), en interculturalidad, en el modelo de la clase invertida o flipped classroom, en enseñanza virtual y creación de OER, en derechos de autor y contenidos multimedia, en humanidades digitales y en ciencias de la computación. Este equipo de trabajo es imprescindible no solo por su multidisciplinariedad y alta especialización, sino también para contar con un número considerable de recursos y materiales didácticos en el OER que hayan sido sometidos a un control de calidad exhaustivo.
    El sitio web está siendo desarrollado por estudiantes de Ingeniería Informática de la Universidad de Granada (Trabajo de Fin de Grado de Miguel González, cuyo trabajo será continuado por estudiantes del programa de prácticas Ícaro) con el adecuado asesoramiento de sus tutores (Silvia Acid Carrillo y Juan Manuel Fernández-Luna).
    Asimismo, contamos con alumnado colaborador del área de lengua española (véase la sección de publicaciones de TFG).
    </p>

